import PDFDocument from 'pdfkit';
import fs from 'node:fs';

const W = 400, H = 720, M = 24;
const ACCENT = '#1971C2', INK = '#1A1B1E', GREY = '#5A5A5A', GREEN = '#1E7A3D', RED = '#C0392B';

let D;
const rule = () => { const y = D.y + 1; D.moveTo(M, y).lineTo(W - M, y).lineWidth(1).strokeColor(ACCENT).opacity(0.45).stroke().opacity(1); D.moveDown(0.5); };
const h1 = (t) => { D.moveDown(0.8); D.font('B').fontSize(14.5).fillColor(ACCENT).text(t.toUpperCase()); rule(); };
const h2 = (t) => { D.moveDown(0.3); D.font('B').fontSize(13).fillColor(INK).text(t); D.moveDown(0.15); };
const p = (t, c) => { D.font('R').fontSize(12.5).fillColor(c || INK).text(t, { lineGap: 2.5 }); D.moveDown(0.3); };
const b = (t, c) => { D.font('R').fontSize(12.5).fillColor(c || INK).text('•  ' + t, { indent: 2, lineGap: 2.5 }); D.moveDown(0.12); };
const qa = (q, a) => { D.font('B').fontSize(12.5).fillColor(INK).text(q, { lineGap: 1.5 }); D.font('R').fontSize(12.5).fillColor(GREY).text('→ ' + a, { lineGap: 2.5 }); D.moveDown(0.4); };
const lab = (t, c) => { D.moveDown(0.2); D.font('B').fontSize(12.5).fillColor(c).text(t); D.moveDown(0.1); };
const title = (sub) => {
  D.font('B').fontSize(26).fillColor(INK).text('BILIM ', { continued: true }).fillColor(ACCENT).text('OS');
  D.font('R').fontSize(12).fillColor(GREY).text(sub); D.moveDown(0.4);
};
const contact = () => { D.moveDown(0.6); D.font('R').fontSize(11).fillColor(GREY).text('Контакт: +996 700 144 043  ·  Asystem@gmail.com  ·  Разработано Asystem'); };

function gen(out, build) {
  return new Promise((res, rej) => {
    D = new PDFDocument({ size: [W, H], margins: { top: M, bottom: M, left: M, right: M } });
    D.registerFont('R', 'C:/Windows/Fonts/arial.ttf');
    D.registerFont('B', 'C:/Windows/Fonts/arialbd.ttf');
    const s = fs.createWriteStream(out);
    D.pipe(s);
    build();
    D.end();
    s.on('finish', () => { console.log('PDF written:', out); res(); });
    s.on('error', rej);
  });
}

